package com.pemex.oportexp.Models.EvaluacionEconomica;

public class PozosProduccion {
    private Double pozosActivos;
    private Double pozosPerforados;
    private Double pozosTerminados;

    // Getters y setters
    public Double getPozosActivos() { return pozosActivos; }
    public void setPozosActivos(Double pozosActivos) { this.pozosActivos = pozosActivos; }

    public Double getPozosPerforados() { return pozosPerforados; }
    public void setPozosPerforados(Double pozosPerforados) { this.pozosPerforados = pozosPerforados; }

    public Double getPozosTerminados() { return pozosTerminados; }
    public void setPozosTerminados(Double pozosTerminados) { this.pozosTerminados = pozosTerminados; }
}